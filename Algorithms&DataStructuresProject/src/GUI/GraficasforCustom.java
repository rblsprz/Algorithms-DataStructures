            add(cp);
            cp.setBounds(500,40,500,400);
            //        JFreeChart chart = createChart(createDataset());

            PDFDocument pdfDoc = new PDFDocument();
            pdfDoc.setTitle("Grafica de " +obtenerCliente()+ " sobre " +name);
