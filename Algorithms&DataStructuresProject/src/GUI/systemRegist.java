    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (txtBuscar.getText().equals("")) {
            mostrarHistorial();
        } else {

            Logic lC = new Logic();
            ArrayList<Hist> array = new ArrayList();

            Hist tempCountries[] = lC.readRegistersFilesHistorialAcciones();
            for (int i = 0; i < tempCountries.length; i++) {
                if (tempCountries[i].getFecha().equals(txtBuscar.getText()) || tempCountries[i].getPersona().equals(txtBuscar.getText())) {
                    array.add(tempCountries[i]);
                }
            }//endfor

            if (array.isEmpty()) {
                JOptionPane.showMessageDialog(null, "NO ENCONTRADO");
            } else {

                String matriz[][] = new String[array.size()][4];

                for (int i = 0; i < array.size(); i++) {
                    matriz[i][0] = array.get(i).getFecha();
                    matriz[i][1] = array.get(i).getHora();
                    matriz[i][2] = array.get(i).getAccion();
                    matriz[i][3] = array.get(i).getPersona();

                    jTable1.setModel(new javax.swing.table.DefaultTableModel(
                            matriz,
                            new String[]{
                                "Fecha", "Hora", "Acci칩n", "Persona"
                            }
                    ));
                }
            }
        }
        txtBuscar.setText("");
    }//GEN-LAST:event_btnMostrarActionPerformed
    public void mostrarHistorial() {
        Logic lC = new Logic();
        ArrayList<Hist> array = new ArrayList();

        Hist tempCountries[] = lC.readRegistersFilesHistorialAcciones();
        for (int i = 0; i < tempCountries.length; i++) {
            array.add(tempCountries[i]);
        }//endfor

        String matriz[][] = new String[array.size()][4];

        for (int i = 0; i < array.size(); i++) {
            matriz[i][0] = array.get(i).getFecha();
            matriz[i][1] = array.get(i).getHora();
            matriz[i][2] = array.get(i).getAccion();
            matriz[i][3] = array.get(i).getPersona();

            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                    matriz,
                    new String[]{
                        "Fecha", "Hora", "Acci칩n", "Persona"
                    }
            ));
        }
    }

