    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        if (txtCedula.getText().equals("") || txtPaciente.getText().equals("") || txtAltura.getText().equals("") || txtEdad.getText().equals("") || txtGrasa.getText().equals("") || txtMasaCorporal.getText().equals("") || txtPeso.getText().equals("") || txtAgua.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Rellene todos los espacios en blanco");
        }//End if 
        else {
            try {
                NotasCliente cita = new NotasCliente(txtCedula.getText(), txtPaciente.getText(), txtA_Notas.getText(), Double.parseDouble(txtPeso.getText()), Double.parseDouble(txtMasaMuscu.getText()), Double.parseDouble(txtGrasa.getText()), Double.parseDouble(txtAltura.getText()), Integer.parseInt(txtEdad.getText()), Double.parseDouble(txtMasaCorporal.getText()), Double.parseDouble(txtAgua.getText()), txtFecha.getText());
                queueQ.Enqueue(cita, 1);
                queue.notasPacienteInsertar(txtCedula.getText(), txtPaciente.getText(), txtA_Notas.getText(), Double.parseDouble(txtPeso.getText()), Double.parseDouble(txtMasaMuscu.getText()), Double.parseDouble(txtGrasa.getText()), Double.parseDouble(txtAltura.getText()), Integer.parseInt(txtEdad.getText()), Double.parseDouble(txtMasaCorporal.getText()), Double.parseDouble(txtAgua.getText()), txtFecha.getText());
                txtA_Notas.setText("");
                txtAltura.setText("");
                txtCedula.setText("");
                txtEdad.setText("");
                txtGrasa.setText("");
                txtPaciente.setText("");
                txtPeso.setText("");
                txtFecha.setText("");
                txtAgua.setText("");
                txtMasaCorporal.setText("");
                txtMasaMuscu.setText("");
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Error al insertar las notas del paciente");
            }
            JOptionPane.showMessageDialog(null, "Notas guardadas");

        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

