    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         try {
                logicDelete em = new logicDelete();
                int d = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar " + txtNombr.getText() + " de este archivo?");
                if(d == 0){
                    em.borrarPersona("registros.txt", txtCedula.getText());
                    JOptionPane.showMessageDialog(null, "ELIMINADO CON EXITO");
                    mostrarRegistros();
                    txtCedula.setText("");
                    txtNombr.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    txtDireccion.setText("");
                    txtTipo.setText("");
                }
            }catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al eliminar");
            }   
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        try {
                Logic lC = new Logic();
                ArrayList<Person> array = new ArrayList();
                String contraseña = "";

                Person tempCountries[] = lC.readRegistersFiles();
                for(int i = 0; i < tempCountries.length; i++){
                    array.add(tempCountries[i]);
                }//endfor

                for(int j = 0; j < array.size(); j++){
                    if(txtNombr.getText().equals(array.get(j).getNombre())){
                       contraseña = array.get(j).getContraseña();
                    }
                }

                logicUpdate em = new logicUpdate();
                int edit = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea modificar a " + txtNombr.getText() + " ?");
                if(edit == 0){
                    em.actualizarPersona(txtCedula.getText() ,txtNombr.getText(), txtApellido.getText(), contraseña, txtDireccion.getText(), Integer.parseInt(txtTipo.getText()), Integer.parseInt(txtTelefono.getText()));
                    JOptionPane.showMessageDialog(null, "MODIFICADO CON EXITO");
                    mostrarRegistros();
                    txtNombr.setText("");
                    txtApellido.setText("");
                    txtTelefono.setText("");
                    txtDireccion.setText("");
                    txtTipo.setText("");
                    txtCedula.setText("");
                }
            }catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al modificar");
            }   
    }//GEN-LAST:event_btnModificarActionPerformed

