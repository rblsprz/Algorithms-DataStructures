    public void mostrarRegistros(){
        Logic lC = new Logic();
        ArrayList<Person> array = new ArrayList();
        
        Person tempCountries[] = lC.readRegistersFiles();
        for(int i = 0; i < tempCountries.length; i++){
            array.add(tempCountries[i]);
        }//endfor
       
       String matriz[][]= new String[array.size()][6];
       
         for (int i = 0; i < array.size(); i++) {
        int rol = array.get(i).getTipo();
        int tel = array.get(i).getTel();
        matriz[i][0]= array.get(i).getCedula();
        matriz[i][1]= array.get(i).getNombre();
        matriz[i][2]= array.get(i).getApellido();
        matriz[i][3]= array.get(i).getDireccion();
        matriz[i][4]= String.valueOf(rol);
        matriz[i][5]= String.valueOf(tel);
        
        jTable3.setModel(new javax.swing.table.DefaultTableModel(
           matriz,
            new String [] {
                "Cedula","Nombre", "Apellido", "Dirección", "Tipo", "Telefono"
            }
        ));
         } 
    }

